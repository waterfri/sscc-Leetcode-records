// #80 删除有序数组中的重复项II
/*
    删除重复元素算法的核心思想是跳过重复元素；删除重复元素的核心逻辑应该是简单地跳过重复的值，而不需要嵌套循环去手动移动所有的元素。
关键点：

	1.	双指针法：k 指针表示当前不重复数组的长度，i 指针遍历数组。
	2.	判断条件：如果当前元素和数组中倒数第二个元素不同，则可以保留当前元素。
	3.	时间复杂度：O(n)，因为我们只需要遍历数组一次。
	4.	空间复杂度：O(1)，只使用了常数额外空间。
    */
int removeDuplicates(int* nums, int numsSize) {
    if (numsSize <= 2) {
        return numsSize; // 如果数组小于等于2，直接返回原数组大小
    }

    int k = 2;  // 从第三个元素开始处理

    for (int i = 2; i < numsSize; i++) {
        if (nums[i] != nums[k - 2]) { // 当前元素与前两个保留元素不同
            nums[k] = nums[i];        // 将当前元素保存到k位置
            k++;                      // 增加k，指向下一个可用位置
        }
    }

    return k; // 返回处理后数组的新长度
}